#!/usr/bin/env python
"""Emit the ensembling appendix LaTeX (macros + tables) from the committed JSON,
so every number is bound to
  artifacts/starting_type_adaptation_v1/analysis/ensembling_point.json
  artifacts/starting_type_adaptation_v1/analysis/ensembling_committee_bootstrap.json
(never hand-typed).

Writes:
  papers/unified-report/generated/ensembling_macros.tex     -- \\Ens* macros
  papers/unified-report/generated/tab_ensembling_gen.tex    -- within-checkpoint methods (tab:ensembling)
  papers/unified-report/generated/tab_ensembling_committee.tex -- cross-model committee (tab:ensembling-committee)
"""
import json, os
ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
A = os.path.join(ROOT, "artifacts", "starting_type_adaptation_v1", "analysis")
GEN = os.path.join(ROOT, "papers", "unified-report", "generated")
P = json.load(open(os.path.join(A, "ensembling_point.json")))
CB = json.load(open(os.path.join(A, "ensembling_committee_bootstrap.json")))

pan = P["panels"]["excl_null"]
m = pan["methods"]
boot = CB["bootstrap"]["deltas"]
comm = CB["committee"]


def d3(x): return f"${x:+.3f}$"      # signed delta
def a3(x): return f"${x:.3f}$"       # absolute


# ---------- macros ----------
def bt(key): return boot[key]["mean"], boot[key]["lcb"], boot[key]["ucb"]
seed_m, seed_l, _ = bt("seedens_gain_trans")
bb_m, bb_l, _ = bt("basesft_vs_base_trans")
bs_t_m, _, _ = bt("basesft_vs_sft_trans")
bs_r_m, bs_r_l, bs_r_u = bt("basesft_vs_sft_rep")

macros = [
    r"% AUTO-GENERATED by experiments/emit_ensembling_tex.py -- do not hand-edit.",
    r"\newcommand{\EnsNCk}{%d}" % pan["n_ck"],
    r"\newcommand{\EnsNFam}{%d}" % pan["n_fam"],
    r"\newcommand{\EnsBaseRep}{%s}" % a3(pan["base"]["rep"]),
    r"\newcommand{\EnsBaseTrans}{%s}" % a3(pan["base"]["trans"]),
    # single fine-tunes
    r"\newcommand{\EnsSFTdRep}{%s}" % d3(m["sft_single"]["drep"]),
    r"\newcommand{\EnsSFTdTrans}{%s}" % d3(m["sft_single"]["dtrans"]),
    r"\newcommand{\EnsKLdRep}{%s}" % d3(m["kl_single"]["drep"]),
    r"\newcommand{\EnsKLdTrans}{%s}" % d3(m["kl_single"]["dtrans"]),
    # seed ensembles (deltas vs own base) + the bootstrapped GAIN vs single
    r"\newcommand{\EnsSeedSFTdTrans}{%s}" % d3(m["sft_seedens"]["dtrans"]),
    r"\newcommand{\EnsSeedKLdTrans}{%s}" % d3(m["kl_seedens"]["dtrans"]),
    r"\newcommand{\EnsSeedGainTrans}{%s}" % d3(seed_m),
    r"\newcommand{\EnsSeedGainTransLCB}{%s}" % d3(seed_l),
    # base + adapter (diversity) ensemble = composition
    r"\newcommand{\EnsBaseSFTdRep}{%s}" % d3(m["base_sft"]["drep"]),
    r"\newcommand{\EnsBaseSFTdTrans}{%s}" % d3(m["base_sft"]["dtrans"]),
    r"\newcommand{\EnsBaseSFTvsBaseTrans}{%s}" % d3(bb_m),
    r"\newcommand{\EnsBaseSFTvsBaseTransLCB}{%s}" % d3(bb_l),
    r"\newcommand{\EnsBaseSFTvsSFTTrans}{%s}" % d3(bs_t_m),
    r"\newcommand{\EnsBaseSFTvsSFTRep}{%s}" % d3(bs_r_m),
    r"\newcommand{\EnsBaseSFTvsSFTRepLCB}{%s}" % d3(bs_r_l),
    r"\newcommand{\EnsBaseSFTvsSFTRepUCB}{%s}" % d3(bs_r_u),
    r"\newcommand{\EnsSFTKLdTrans}{%s}" % d3(m["sft_kl"]["dtrans"]),
    # committee (cross-model) transfer vs best single guard
    r"\newcommand{\EnsCommSFTvsBestGen}{%s}" % d3(comm["general"]["sft"]["vs_best_single_trans"]),
    r"\newcommand{\EnsCommSFTvsBestAll}{%s}" % d3(comm["all10"]["sft"]["vs_best_single_trans"]),
    r"\newcommand{\EnsCommKLvsBestGen}{%s}" % d3(comm["general"]["kl_sft"]["vs_best_single_trans"]),
    r"\newcommand{\EnsCommKLvsBestAll}{%s}" % d3(comm["all10"]["kl_sft"]["vs_best_single_trans"]),
]
open(os.path.join(GEN, "ensembling_macros.tex"), "w").write("\n".join(macros) + "\n")

# ---------- within-checkpoint methods table ----------
LAB = {
    "sft_single": r"SFT (single run)", "sft_seedens": r"SFT, 5-seed ensemble",
    "kl_single": r"KL-SFT (single run)", "kl_seedens": r"KL-SFT, 5-seed ensemble",
    "base_sft": r"base $\oplus$ SFT \; \footnotesize(= Act~II)\normalsize",
    "base_kl": r"base $\oplus$ KL-SFT", "sft_kl": r"SFT $\oplus$ KL-SFT",
    "base_sft_kl": r"base $\oplus$ SFT $\oplus$ KL-SFT",
}
NOTE = {
    "sft_single": r"specializes", "sft_seedens": r"denoise; still $<$ base",
    "kl_single": r"mild specialize", "kl_seedens": r"$\approx$ base",
    "base_sft": r"\textbf{recovers transfer}", "base_kl": r"recovers, lower rep",
    "sft_kl": r"no transfer help", "base_sft_kl": r"$\approx$ base$\oplus$SFT",
}


def row(key):
    e = m[key]
    return r"%s & %s & %s & %s & %s & %s \\" % (
        LAB[key], a3(e["rep"]), d3(e["drep"]), a3(e["trans"]), d3(e["dtrans"]), NOTE[key])


tbl = [
    r"% AUTO-GENERATED by experiments/emit_ensembling_tex.py -- do not hand-edit.",
    r"\begin{table}[t]\centering\small",
    r"\caption{Retrospective ensembling on the adaptation panel (\EnsNCk{} checkpoints across \EnsNFam{}",
    r"families, excl.\ the degenerate Llama-Guard null cell; equal-family macro-AP on the raw margin,",
    r"anchored to each checkpoint's \emph{own} unmodified base). Within-checkpoint ensembles average raw",
    r"margins (scale-comparable, shared head). Represented $=$ id\_test, transfer $=$ transfer\_test.",
    r"Ensembling a fine-tune \emph{with itself} (seeds) only denoises; only adding the \emph{un-tuned base}",
    r"lifts transfer above base.}",
    r"\label{tab:ensembling}",
    r"\begin{tabular}{@{}l cc cc l@{}}",
    r"\toprule",
    r"& \multicolumn{2}{c}{represented} & \multicolumn{2}{c}{transfer} & \\",
    r"\cmidrule(lr){2-3}\cmidrule(lr){4-5}",
    r"Guard / ensemble & AP & $\Delta$ & AP & $\Delta$ & effect \\",
    r"\midrule",
    r"unmodified base & \EnsBaseRep{} & --- & \EnsBaseTrans{} & --- & reference \\",
    r"\midrule",
    r"\multicolumn{6}{@{}l}{\emph{Ensemble a fine-tune with itself (redundant $\Rightarrow$ correlated errors)}} \\",
    row("sft_single"), row("sft_seedens"), row("kl_single"), row("kl_seedens"),
    r"\midrule",
    r"\multicolumn{6}{@{}l}{\emph{Ensemble across the specialization axis (diverse $\Rightarrow$ decorrelated errors)}} \\",
    row("base_sft"), row("base_kl"), row("sft_kl"), row("base_sft_kl"),
    r"\bottomrule",
    r"\end{tabular}",
    r"\end{table}",
]
open(os.path.join(GEN, "tab_ensembling_gen.tex"), "w").write("\n".join(tbl) + "\n")

# ---------- committee table ----------
def crow(panel, adap, lab):
    e = comm[panel][adap]
    return r"%s & %s & %s & %s & %s \\" % (
        lab, a3(e["rep"]), a3(e["trans"]), a3(e["best_single_trans"]), d3(e["vs_best_single_trans"]))


ctbl = [
    r"% AUTO-GENERATED by experiments/emit_ensembling_tex.py -- do not hand-edit.",
    r"\begin{table}[t]\centering\small",
    r"\caption{Cross-model \emph{committee} ensembles (rank-percentile average of different guards on the",
    r"same row). A committee of SFT guards does not beat the single best guard on transfer --- SFT",
    r"correlates their off-distribution errors. Only the less-specialized KL-SFT committee breaks even.}",
    r"\label{tab:ensembling-committee}",
    r"\begin{tabular}{@{}l cc c r@{}}",
    r"\toprule",
    r"Committee & rep.\ AP & transfer AP & best single (tr.) & $\Delta$ vs best \\",
    r"\midrule",
    r"\multicolumn{5}{@{}l}{\emph{4 general checkpoints}} \\",
    crow("general", "unmodified", "committee of bases"),
    crow("general", "sft", "committee of SFT guards"),
    crow("general", "kl_sft", "committee of KL-SFT guards"),
    r"\midrule",
    r"\multicolumn{5}{@{}l}{\emph{all 10 checkpoints}} \\",
    crow("all10", "unmodified", "committee of bases"),
    crow("all10", "sft", "committee of SFT guards"),
    crow("all10", "kl_sft", "committee of KL-SFT guards"),
    r"\bottomrule",
    r"\end{tabular}",
    r"\end{table}",
]
open(os.path.join(GEN, "tab_ensembling_committee.tex"), "w").write("\n".join(ctbl) + "\n")
print("wrote ensembling_macros.tex + tab_ensembling_gen.tex + tab_ensembling_committee.tex")
