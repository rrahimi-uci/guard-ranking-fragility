#!/usr/bin/env python
"""Emit the adaptation-study LaTeX (table + macros) from the committed analysis, so every number in
the paper is bound to artifacts/starting_type_adaptation_v1/analysis/results.json (never hand-typed).

Writes:
  papers/unified-report/generated/adaptation_macros.tex   -- \\Ada* macros (H values + LCBs + verdicts)
  papers/unified-report/generated/tab_adaptation_gen.tex  -- per-checkpoint movement table (tab:adaptation)
"""
import json, os, sys
ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
A = os.path.join(ROOT, "artifacts", "starting_type_adaptation_v1", "analysis")
GEN = os.path.join(ROOT, "papers", "unified-report", "generated")
r = json.load(open(os.path.join(A, "results.json")))
cc = json.load(open(os.path.join(A, "claim_checks.json")))
H = r["point_estimates"]["H"]
mv = r["point_estimates"]["movement_vectors"]
cfg = r["config"]

import yaml
reg = yaml.safe_load(open(os.path.join(ROOT, "configs", "starting_type_adaptation_v1.yaml")))
gtype = {}
for k in reg.get("general_checkpoints", {}): gtype[k] = "general"
for k in reg.get("purpose_built_checkpoints", {}): gtype[k] = "purpose"
LABEL = {  # short display names
    "qwen25_15b": "Qwen2.5-1.5B", "smollm2_17b": "SmolLM2-1.7B", "smollm3_3b": "SmolLM3-3B",
    "qwen3_4b": "Qwen3-4B", "qwen3guard_gen_06b": "Qwen3Guard-0.6B", "qwen3guard_gen_4b": "Qwen3Guard-4B",
    "granite_guardian_31_2b": "Granite-Guard-2B", "shieldgemma_2b": "ShieldGemma-2B",
    "llama_guard_3_1b": "Llama-Guard-3-1B", "wildguard_7b": "WildGuard-7B",
}

def s(x): return f"${x:+.3f}$"

# ---- macros ----
rq1, rq2 = cc["RQ1"], cc["RQ2"]
macros = [
    r"% AUTO-GENERATED by experiments/emit_adaptation_tex.py -- do not hand-edit.",
    r"\newcommand{\AdaNCheckpoints}{%d}" % cfg["n_checkpoints"],
    r"\newcommand{\AdaNFamilies}{%d}" % len(cfg["model_families"]),
    r"\newcommand{\AdaPrimaryBeta}{%.1f}" % cfg["primary_beta"],
    r"\newcommand{\AdaMargin}{%.2f}" % cfg["non_inferiority_margin_m"],
    r"\newcommand{\AdaHGain}{$%+.3f$}" % H["H_gain"],
    r"\newcommand{\AdaHGainLCB}{$%+.3f$}" % rq1["H_gain_lcb"],
    r"\newcommand{\AdaHConc}{$%+.3f$}" % H["H_conc"],
    r"\newcommand{\AdaHConcLCB}{$%+.3f$}" % rq1["H_conc_lcb"],
    r"\newcommand{\AdaHPreserve}{$%+.3f$}" % H["H_preserve"],
    r"\newcommand{\AdaHPreserveLCB}{$%+.3f$}" % rq2["H_preserve_lcb"],
    r"\newcommand{\AdaHCost}{$%+.3f$}" % H["H_cost"],
    r"\newcommand{\AdaHCostLCB}{$%+.3f$}" % rq2["H_cost_lcb"],
    r"\newcommand{\AdaHheldSFT}{$%+.3f$}" % H["H_held_sft"],
    r"\newcommand{\AdaRQoneSupported}{%s}" % ("true" if rq1["supported"] else "false"),
    r"\newcommand{\AdaRQtwoSupported}{%s}" % ("true" if rq2["supported"] else "false"),
]
open(os.path.join(GEN, "adaptation_macros.tex"), "w").write("\n".join(macros) + "\n")

# ---- per-checkpoint movement table ----
order = sorted(mv, key=lambda k: (gtype.get(k, "z") != "general", gtype.get(k, "z"), k))
rows = []
for k in order:
    v = mv[k]; sft = v["theta_sft"]; kl = v["theta_kl"]
    rows.append(r"%s & %s & %s & %s & %s & %s \\" % (
        LABEL.get(k, k), v["model_family"], s(sft[0]), s(sft[1]), s(kl[0]), s(kl[1])))

tbl = [
    r"% AUTO-GENERATED by experiments/emit_adaptation_tex.py -- do not hand-edit.",
    r"\begin{table}[htbp]\centering\small",
    r"\caption{Adaptation-study movement vectors: change in macro-AP (RAW-margin, tie-aware) vs.\ the",
    r"\emph{same} unmodified checkpoint on represented (id\_test) and held-out (transfer) sources, for",
    r"ordinary SFT and KL-SFT ($\beta{=}\AdaPrimaryBeta$), averaged over 5 seeds. Positive represented /",
    r"negative held-out $=$ specialization. Both deltas are within-checkpoint (the KL reference is that",
    r"checkpoint). Bounds are fixed-panel (\Cref{sec:adaptation}).}",
    r"\label{tab:adaptation}",
    r"\begin{tabular}{@{}l l cc cc@{}}",
    r"\toprule",
    r"& & \multicolumn{2}{c}{SFT $\Delta$} & \multicolumn{2}{c}{KL-SFT $\Delta$} \\",
    r"\cmidrule(lr){3-4}\cmidrule(lr){5-6}",
    r"Checkpoint & Family & repr. & transfer & repr. & transfer \\",
    r"\midrule",
    r"\multicolumn{6}{@{}l}{\emph{General instruction checkpoints}} \\",
]
for k, row in zip(order, rows):
    if gtype.get(k) == "purpose" and "purpose_header" not in tbl:
        tbl.append(r"\midrule")
        tbl.append(r"\multicolumn{6}{@{}l}{\emph{Released purpose-built guards}} \\")
        tbl.append("purpose_header")
    tbl.append(row)
tbl = [x for x in tbl if x != "purpose_header"]
tbl += [r"\bottomrule", r"\end{tabular}", r"\end{table}"]
open(os.path.join(GEN, "tab_adaptation_gen.tex"), "w").write("\n".join(tbl) + "\n")
print("wrote adaptation_macros.tex + tab_adaptation_gen.tex")
print("H_gain=%.3f (LCB %.3f) H_conc=%.3f (LCB %.3f) H_preserve=%.3f (LCB %.3f) H_cost=%.3f (LCB %.3f)" % (
    H["H_gain"], rq1["H_gain_lcb"], H["H_conc"], rq1["H_conc_lcb"],
    H["H_preserve"], rq2["H_preserve_lcb"], H["H_cost"], rq2["H_cost_lcb"]))
print("RQ1 supported:", rq1["supported"], "| RQ2 supported:", rq2["supported"])
